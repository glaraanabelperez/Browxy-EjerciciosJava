public class MiPrograma {

  public static void main(String[] args) {
      System.out.println(Boolean.parseBoolean("true"));
      System.out.println(Byte.parseByte("23"));
      System.out.println(Short.parseShort("432"));
      System.out.println(Integer.parseInt("234"));
      System.out.println(Long.parseLong("20000000"));
      System.out.println(Float.parseFloat("5.6"));
      System.out.println(Double.parseDouble("78.23423"));
   }
}

